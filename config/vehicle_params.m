function p = vehicle_params()
%% vehicle_params.m — 14-DOF 四轮独立驱动/转向 底盘动力学模型参数
% 所有公式中只使用字母符号，不出现具体数字
% 数字仅在参数赋值处出现，便于替换为任意车辆参数
%
% 14自由度:
%   车身: 纵向/横向/垂向/侧倾/俯仰/横摆 (6DOF)
%   车轮垂向: 四轮簧下质量垂向跳动 (4DOF)
%   车轮旋转: 四轮独立旋转 (4DOF)
%
% 29状态变量:
%   vx, vy, z, vz, phi, phi_dot, theta, theta_dot, psi_dot,
%   z_w(1:4), z_w_dot(1:4), omega_w(1:4),
%   Fx_relax(1:4), Fy_relax(1:4)
%
% 11控制输入:
%   delta(1:4)  — 四轮独立转向角 [rad]
%   T_m(1:4)    — 四轮独立电机扭矩 [N·m]
%   beta_brk    — 制动踏板开度 [0~1]
%   i_h, i_c    — 横向/纵向坡度比 [-]
%
% 车轮编号: 1=FL(左前), 2=FR(右前), 3=RL(左后), 4=RR(右后)

%% ===================== 整车质量参数 =====================
p.M    = 1500;       % 整车总质量 [kg]
p.m_s  = 1350;       % 簧上质量 [kg]
p.m_uf = 35;         % 前轴非簧载质量（单轮）[kg]
p.m_ur = 40;         % 后轴非簧载质量（单轮）[kg]

%% ===================== 几何参数 =====================
p.a  = 1.2;          % 质心至前轴水平距离 [m]
p.b  = 1.3;          % 质心至后轴水平距离 [m]
p.L  = p.a + p.b;    % 轴距 [m] = 2.5
p.B_f = 1.52;        % 前轮距 [m]
p.B_r = 1.52;        % 后轮距 [m]
p.B_sf = 1.2;        % 前悬架安装点横向间距 [m]
p.B_sr = 1.2;        % 后悬架安装点横向间距 [m]
p.h_g   = 0.55;      % 整车质心高度 [m]
p.h_rcf = 0.08;      % 前轴侧倾中心高度 [m]
p.h_rcr = 0.10;      % 后轴侧倾中心高度 [m]
% 重心处侧倾轴线高度 = h_rcf + (h_rcr - h_rcf) * a / L ≈ 0.09 m
% 重心至侧倾轴线垂直距离 = h_g - 0.09 ≈ 0.46 m（重力项与惯性项共用同一几何力臂）
p.d_roll  = 0.46;    % 重心至侧倾轴线垂直距离 [m]（合并原 h_phi 与 C0）
p.d_pitch = 0.46;    % 重心至俯仰轴线垂直距离 [m]（合并原 h_theta 与 F0）

%% ===================== 转动惯量 =====================
p.I_x  = 450;        % 簧上质量绕侧倾轴转动惯量 [kg·m²]
p.I_y  = 2200;       % 簧上质量绕俯仰轴转动惯量 [kg·m²]
p.I_z  = 2500;       % 整车绕横摆轴转动惯量 [kg·m²]
p.I_w  = 1.5;        % 单轮转动惯量 [kg·m²]
p.I_xz = 0;          % 惯性积 I_xz [kg·m²]（通常较小，可设0）

%% ===================== 悬架参数（非线性模型D） =====================
% 弹簧力: F_spring = K_s1 * stroke + K_s2 * stroke³
% 阻尼力: F_damper = C_s1 * stroke_dot + C_s2 * |stroke_dot| * stroke_dot
% 总悬架力: F_s = F_s0 + F_spring + F_damper
%
% 前悬架
p.K_s1_f = 22000;    % 前悬架线性刚度 [N/m]
p.K_s2_f = 4.0e6;    % 前悬架三次刚度 [N/m³]（~80mm行程时三次力≈线性力，模拟缓冲块）
p.C_s1_f = 2800;     % 前悬架名义线性阻尼 [N·s/m]（回弹/压缩平均值）
p.C_s2_f = 800;      % 前悬架平方阻尼 [N·s²/m²]（速度超线性增长）
p.xi_reb_comp = 2.75; % 回弹/压缩阻尼比（rebound/compression ratio, >1=回弹更硬）
p.K_d_susp = 15;       % 压缩/回弹 sigmoid 过渡锐度 [s/m]（15 = ~0.07m/s过渡区）
% 后悬架
p.K_s1_r = 20000;    % 后悬架线性刚度 [N/m]
p.K_s2_r = 3.5e6;    % 后悬架三次刚度 [N/m³]（~80mm行程时三次力≈线性力）
p.C_s1_r = 2500;     % 后悬架名义线性阻尼 [N·s/m]
p.C_s2_r = 700;      % 后悬架平方阻尼 [N·s²/m²]

%% ===================== 轮胎参数 =====================
% 垂向刚度/阻尼
p.K_t  = 210000;     % 轮胎垂向刚度 [N/m]
p.C_t  = 5000;       % 轮胎垂向阻尼 [N·s/m]（增大以抑制垂向高频振荡）
p.r_w  = 0.31;       % 车轮有效滚动半径 [m]

% --- Magic Formula 侧偏参数 ---
p.B_y  = 8.0;        % 侧偏刚度因子 [1/rad]
p.C_y  = 1.30;       % 侧偏形状因子
p.D_y  = 4200;       % 侧偏峰值因子基准值 [N]（μ_y ≈ D_y/Fz0 ≈ 1.05，干沥青典型值）
p.E_y  = -1.6;       % 侧偏曲率因子
p.n_Fz = 0.80;       % 垂向载荷灵敏度指数 (0.7~0.9)

% --- Magic Formula 纵滑参数 ---
p.B_x  = 12.0;       % 纵滑刚度因子
p.C_x  = 1.65;       % 纵滑形状因子
p.D_x  = 4200;       % 纵滑峰值因子基准值 [N]（μ_x ≈ D_x/Fz0 ≈ 1.05，干沥青典型值）
p.E_x  = -1.2;       % 纵滑曲率因子
p.Fz0  = 4000;       % 参考垂向载荷 [N]

% --- 摩擦椭圆 ---
p.mu_x = p.D_x / p.Fz0;   % 纵向峰值摩擦系数
p.mu_y = p.D_y / p.Fz0;   % 侧向峰值摩擦系数

% --- 轮胎松弛长度（纵滑与侧偏独立）---
p.sigma_x = 0.30;         % 纵滑松弛长度 [m]（与原sigma一致，保持向后兼容）
p.sigma_y = 0.60;         % 侧偏松弛长度 [m]（通常为纵滑的2~3倍）

%% ===================== 轮毂电机参数 =====================
% 四轮独立驱动: T_drive(i) = T_m(i)（直接施加于各轮）
p.T_m_max    = 300;       % 电机峰值扭矩 [N·m]
p.T_m_regen_max = 100;       % 电机最大回馈制动扭矩 [N*m]（约1/3峰值）
p.T_m_rated  = 150;       % 电机额定扭矩 [N·m]
p.P_m_rated  = 30000;     % 电机额定功率 [W]
p.omega_m_max = 1200;     % 电机最高转速 [rad/s]（约 11460 rpm）

%% ===================== 制动系参数 =====================
p.d_brake_master = 0.0222;    % 制动主缸直径 [m]
p.i_brake_pedal  = 4.1;       % 制动踏板机构传动比
p.eta_brake_pedal = 0.86;     % 踏板及主缸机械效率
p.k_brake_boost  = 3.5;       % 真空助力比
p.d_brake_caliper_f = 0.04789;% 前制动钳活塞直径 [m]
p.d_brake_caliper_r = 0.0329; % 后制动钳活塞直径 [m]
p.eta_brake_caliper = 0.85;   % 制动钳机械效率
p.mu_brake_pad  = 0.35;       % 制动片摩擦系数
p.r_brake_disc_f = 0.105;     % 前制动盘有效摩擦半径 [m]
p.r_brake_disc_r = 0.113;     % 后制动盘有效摩擦半径 [m]
p.F_pedal_max   = 300;        % 最大制动踏板力 [N]

%% ===================== 转向系参数 =====================
% 四轮独立转向，各轮转向角 delta_i 作为直接控制输入
% 不再需要转向传动比（转向由上层控制器给定）
% 保留传动比仅用于向后兼容和人机界面映射
p.i_st = 18;           % 方向盘至车轮转角传动比（参考值）

%% ===================== 行驶阻力参数 =====================
p.g       = 9.81;      % 重力加速度 [m/s²]
p.f_roll  = 0.015;     % 滚动阻力系数
p.rho_air = 1.225;     % 空气密度 [kg/m³]
p.C_d     = 0.30;      % 空气阻力系数
p.A_f     = 2.2;       % 迎风面积 [m²]

%% ===================== 坡度限制 =====================
p.slope_max_deg = 30;                      % 最大坡度角 [度]
p.slope_max_rad = p.slope_max_deg * pi / 180;  % ≈ 0.5236 rad
p.slope_max     = tan(p.slope_max_rad);    % 最大坡度比 ≈ 0.577

%% ===================== 数值参数 =====================
p.eps_v   = 0.2;       % 最低速度保护 [m/s]（防止低速奇异，坡道起步场景需更低值）
p.eps_gnd = 1e-3;      % 地面接触光滑过渡宽度 [m]
p.inv_tau_min = 5.0;   % 最小松弛频率 [1/s]（提高以改善低速/坡起时轮胎力响应速度）
p.T_ramp = 1.0;        % 控制输入平滑过渡时间 [s]（九阶C⁴连续多项式，典型值0.5~2.0）

%% ===================== 仿真默认初始条件 =====================
p.vx0 = 30 / 3.6;      % 初始车速 [m/s]（30 km/h）
p.Kp_speed = 1000;     % 车速P控制器增益 [N·m/(m/s)]
p.Kp_brake = 0.75;   % 制动协调增益 [1/(m/s)]（下坡控速时自动补充摩擦制动）
p.brake_deadband = 0.03;      % 自动摩擦制动超速死区 [m/s]（避免目标附近频繁介入）
p.brake_cruise_deadband = 0.50; % 平路/上坡巡航制动死区 [m/s]（避免小超调时频繁摩擦制动）
p.beta_brk_auto_max = 0.60;   % 自动摩擦制动最大踏板开度（保留人工制动余量）
p.use_dyn_load_transfer = false;  % 动态载荷转移开关（true=混合悬架动态力到MF垂向力）

%% ===================== 参数一致性自动校验 =====================
M_check = p.m_s + 2*p.m_uf + 2*p.m_ur;   % 簧上质量 + 四轮簧下质量
M_err = abs(p.M - M_check);
if M_err > 1e-6
    error('质量参数不一致: 总质量 M=%.1f kg ≠ 簧上质量 m_s + 2*m_uf + 2*m_ur = %.1f kg (差值 %.1f kg)', ...
          p.M, M_check, M_err);
end
fprintf('参数校验通过: M = m_s + 2×m_uf + 2×m_ur = %.1f kg ✓\n', p.M);

fprintf('14-DOF 四轮独立驱动/转向 参数已加载。\n');
fprintf('  驱动: 四轮独立轮毂电机\n');
fprintf('  转向: 四轮独立转向\n');
fprintf('  悬架: 非线性模型D (三次弹簧 + 平方阻尼)\n');
fprintf('  轮胎: Magic Formula + 摩擦椭圆 + 松弛长度\n');

%% ===== 加载扭矩分配控制参数 =====
p.ctrl = control_params();
end
